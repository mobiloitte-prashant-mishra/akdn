/**
 * Override or insert variables into the page templates.
 *
 * @param $variables
 *   An array of variables to pass to the theme template.
 * @param $hook
 *   The name of the template being rendered ("page" in this case.)
 */
function iis_preprocess_page(&$variables, $hook) {
  // Add js file.
  $path = drupal_get_path('module', 'iis_custom');
  drupal_add_js($path . '/js/iis_custom.js', array('type' => 'file', 'scope' => 'footer'));
  // Remove the page title from graduate studies and research page.
  if (arg(0) == 'graduate-studies' || arg(0) == 'research') {
    $variables['title'] = '';
  }
}

/**
 * Override or insert variables into the node templates.
 *
 * @param $variables
 *   An array of variables to pass to the theme template.
 * @param $hook
 *   The name of the template being rendered ("node" in this case.)
 */
function iis_preprocess_node(&$variables, $hook) {
  $node_path = drupal_get_path_alias('node/' . $variables['nid']);
  $path = drupal_get_path('module', 'iis_custom');
  if ($node_path == 'subscribe') {
    drupal_add_js($path . '/js/subscribe.js', array('type' => 'file', 'scope' => 'footer'));
  }
}


