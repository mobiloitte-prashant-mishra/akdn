/**
 * Preprocess views fields.
 * @param $vars
 *   An array of variables to pass to theme template.
 */
function iis_preprocess_views_view_fields(&$vars) {
  $view = $vars['view'];
  if ($view->name == 'publications' && $view->current_display == 'block_4') {
    if (isset($vars['fields']['field_author'])) {
      $field_output = $view->style_plugin->get_field($view->row_index, 'field_author');
      $vars['fields']['field_author']->content = strip_tags($field_output);
    }
  }
}

